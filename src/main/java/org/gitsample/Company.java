package org.gitsample;

public class Company {
	
	private String name;
	private int salary;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	private int getSalary() {
		return salary;
	}
	private void setSalary(int salary) {
		this.salary = salary;
	}
	
}
